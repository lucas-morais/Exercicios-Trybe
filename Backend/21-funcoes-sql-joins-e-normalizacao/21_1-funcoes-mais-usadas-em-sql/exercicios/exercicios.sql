#9
SELECT 
	JOB_ID, 
    AVG(SALARY) AS `SALARY_AVG`
FROM 
	hr.employees
GROUP BY JOB_ID
HAVING JOB_ID <> 'IT_PROG'
ORDER BY SALARY_AVG DESC;

#10
SELECT
	DEPARTMENT_ID,
    AVG(SALARY) AS `aVG_SALARY`,
    COUNT(*) AS `NUMBER_OF_EMPLOYESS`
FROM
	hr.employees
GROUP BY
	DEPARTMENT_ID
HAVING NUMBER_OF_EMPLOYESS > 10;

